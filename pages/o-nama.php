  <h2>Nešto kratko o samoj udruzi</h2>

  <p>
    Zabočki lavovi su sportska udruga osnovana s ciljem promicanja zdravog života i sportskih aktivnosti u lokalnoj zajednici.
    Svi članovi su na neki način povezani sa sportom, bilo da se njime i dalje aktivno bave
    (nogomet, košarka, kuglanje…) ili su se nekada bavili nekim sportom.
    Vesela smo družina puna dobrog i pozitivnog duha, s ciljem da se sport svakim danom sve više promovira
    i pokaže koliko je on bitan dio naših života.
  </p>

  <p>
    Naša udruga okuplja ljude svih generacija koji dijele ljubav prema sportu i zdravom načinu života,
    jer kako se kaže – u zdravom tijelu, zdrav duh.
    Budite aktivni i krećite se koliko god je to moguće.
  </p>

  <p>
    Organiziramo različite događaje, turnire i radionice kako bismo osigurali aktivan i kvalitetan stil života
    za sve naše članove. Svake godine organizira se ljetni turnir u nogometu pod nazivom
    <strong>„Kup kvartova“</strong> te zimski turnir <strong>„Zagorska zipka“</strong>.
  </p>

